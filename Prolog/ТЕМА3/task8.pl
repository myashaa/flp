% ?- Определите процедуру combi(L1,L2,L3), которая помещает элементы второго списка между элементами первого
% ?- combi([1,2,3,4],[a,b],L3).
combi([], [], []).
combi([X|L1], [], [X|L3]) :- 
  combi(L1, [], L3).
combi([], [Y|L2], [Y|L3]) :- 
  combi([], L2, L3).
combi([X|L1], [Y|L2], [X,Y|L3]) :- 
  combi(L1, L2, L3).
